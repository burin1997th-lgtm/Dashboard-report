// ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Å‡∏≤‡∏£‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏Å‡∏≤‡∏£
function handleAction(action) {
    switch(action) {
        case 'preview':
            showPreview();
            break;
        case 'analyze':
            analyzeData();
            break;
        case 'graph':
            createGraph();
            break;
        case 'download':
            downloadData();
            break;
        case 'delete':
            deleteFile();
            break;
    }
}

